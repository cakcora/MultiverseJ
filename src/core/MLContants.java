package core;

public class MLContants {

	/**
	 * Constant value for the epsilon that is used for ignoring rounding errors in
	 * floating point arithmetic.
	 */
	public static final double EPSILON = Math.pow(10, -6);
	
	/**
	 * Precise constant value for the epsilon that is used for ignoring rounding errors in
	 * floating point arithmetic.
	 */
	public static final double PRECISE_EPSILON = Math.pow(10, -10);

}
